export const settingsPersistenceApi = {
  get: () => fetchJson('/api/settings'),
  import: (settings) => fetchJson('/api/settings/import', {
    method: 'POST',
    body: JSON.stringify(settings),
  }),
  update: (settings) => fetchJson('/api/settings', {
    method: 'POST',
    body: JSON.stringify(settings),
  }),
  clearDatabase: (options) => fetchJson('/api/database/clear', {
    method: 'POST',
    body: JSON.stringify(options),
  }),
};
